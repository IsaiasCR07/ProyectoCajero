    private void Boton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Boton4MouseClicked
        textoBoton4 = Boton4.getText();

        if (textoBoton4.equals("Acceder Cajero")) {
            JOptionPane.showMessageDialog(null, "Ingrese su tarjeta");
            TextoCajero.setText(" Ingrese su Pin: ");
            Pin.setVisible(true);
            Boton2.setVisible(false);
            Boton3.setVisible(false);
            Boton4.setText("Ingresar");
            Boton5.setVisible(true);
        }

        if (textoBoton4.equals("Ingresar")) {
            textoPin = Pin.getText();
            pinCorrecto = false;
            pinCorrecto = r.verificarPin(Integer.parseInt(textoPin));

            if (pinCorrecto) {
                Pin.setVisible(false);
                TextoCajero.setText(" Seleccione una opcion: ");
                Boton1.setVisible(true);
                Boton1.setText("Extraer Dinero");
                Boton2.setVisible(true);
                Boton2.setText("Depositar Dinero");
                Boton3.setVisible(true);
                Boton3.setText("Consultar Saldo");
                Boton4.setText("Cambio de Pin");
            }
            if (!pinCorrecto) {
                Pin.setText("INCORRECTO");
                TextoCajero.setText(" Pin Incorrecto. Intente de nuevo: ");

            }
        }
        if (textoBoton4.equals("Cambio de Pin")) {
            TextoCajero.setText(" Ingrese su Pin: ");
            Pin.setVisible(true);
            Boton1.setVisible(false);
            Boton2.setVisible(false);
            Boton3.setVisible(false);
            Boton4.setText("Ingresar PIN actual");
        }

        if (textoBoton4.equals("Ingresar PIN actual")) {
            textoPin = Pin.getText();
            pinCorrecto = false;
            pinCorrecto = r.verificarPin(Integer.parseInt(textoPin));
            if (pinCorrecto) {
                TextoCajero.setText(" Ingrese su nuevo Pin: ");
                Boton4.setText("Ingresar nuevo PIN");
            }
            if (!pinCorrecto) {
                Pin.setText("INCORRECTO");
                TextoCajero.setText(" Pin Incorrecto. Intente de nuevo: ");
            }
        }

        if (textoBoton4.equals("Ingresar nuevo PIN")) {
            String textoNuevoPin = Pin.getText();
            r.cambiarPin((Integer.parseInt(textoPin)), (Integer.parseInt(textoNuevoPin)));
            JOptionPane.showMessageDialog(null, "Pin fue modificado exitosamente");
            cargarMenuPrincipal();

        }
        
        if (textoBoton4.equals("$50")) {
            boolean seExtrajo = extraerDinero(50,(Integer.parseInt(textoPin)));
            if(seExtrajo){
                JOptionPane.showMessageDialog(null, "Tome su dinero");
                cargarMenuPrincipal();
            }
            if(!seExtrajo){
                JOptionPane.showMessageDialog(null, "Fondos insuficientes!"); 
                cargarMenuPrincipal();
            }
        }
        
        if (textoBoton4.equals("Depositar")) {
            int cantidad = Integer.parseInt(Boton6.getText());
            JOptionPane.showMessageDialog(null, "Ingrese el dinero al cajero"); 
            r.depositarDinero(cantidad, (Integer.parseInt(textoPin)));
            cargarMenuPrincipal();
        }

    }//GEN-LAST:event_Boton4MouseClicked

